#include "3-calc.h"

/**
 *op_add - add 2 integers
 *@x: the first int number
 *@y: the second int number
 *Return: the sum
*/

int op_add(int x, int y)
{
return (x + y);
}

/**
 *op_sub - substract 2 integers
 *@x: the first int number
 *@y: the second int number
 *Return: the substraction(difference)
*/

int op_sub(int x, int y)
{
return (x - y);
}

/**
 *op_mul - multiply 2 integers
 *@x: the first int number
 *@y: the second int number
 *Return: the multiplification
*/

int op_mul(int x, int y)
{
return (x * y);
}

/**
 *op_div - divide 2 integers
 *@x: the first int number
 *@y: the second int number
 *Return: the quotient
*/

int op_div(int x, int y)
{
return (x / y);
}

/**
 *op_mod - modulo 2 integers
 *@x: the first int number
 *@y: the second int number
 *Return: remainder
*/

int op_mod(int x, int y)
{
return (x % y);
}
